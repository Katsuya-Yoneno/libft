#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	output;

	if (!dst && !src)
		return (0);
	output = 0;
	while (src[output] != '\0')
		output++;
	if (size == 0)
		return (output);
	i = 0;
	while (src[i] != '\0' && i < (size - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i++] = '\0';
	return (output);
}
