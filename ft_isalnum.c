#include <stdio.h>

int	ft_isalnum(int c)
{
	int	res;

	if ('0' <= c && c <= '9')
		res = c;
	else if ('A' <= c && c <= 'Z')
		res = c;
	else if ('Z' <= c && c <= 'z')
		res = c;
	else
		return (0);
	return (res);
}
