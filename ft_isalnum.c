#include <stdio.h>

int	ft_isalnum(int c)
{
	int	res;

	if (48 <= c && c <= 57)
		res = c;
	else if (65 <= c && c <= 90)
		res = c;
	else if (97 <= c && c <= 122)
		res = c;
	else
		return (0);
	return (res);
}
