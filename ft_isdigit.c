int	ft_isdigit(char *str)
{
	int	i;
	int	res;

	i = 0;
	if (str[0] == '\0')
		res = 1;
	while (str[i] != '\0')
	{
		if ('0' <= str[i] && str[i] <= '9')
			res = 1;
		else
		{
			res = 0;
			break ;
		}
		i++;
	}
	return (res);
}
